package oops_concepts;

public class Students {

	public String name;
	public int age;
	public String grade;

	public void getName() {
		System.out.println("Name is " + name);
	}

	public void getAge() {
		System.out.println("Age of the " + name + " is " + age);
	}

	public void getGrade() {
		System.out.println("Grade of the " + name + " is " + grade);
	}
	
}
